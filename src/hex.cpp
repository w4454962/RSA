std::string Sha1(const std::string& input)
{
    unsigned char sha1out[SHA1HashSize] = { 0 };
    SHA1Context context;
    SHA1Reset(&context);
    SHA1Input(&context, (const unsigned char*)input.c_str(), input.length());
    SHA1Result(&context, sha1out);
   
    return BinToHex(std::string((const char*)sha1out, SHA1HashSize));
}

