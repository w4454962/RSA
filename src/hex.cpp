std::string BinToHex(const std::string& bin)
{
    static char hexstr[] = "0123456789ABCDEF";
    std::string hex;
    size_t hexlen = bin.length() * 2;
    hex.resize(hexlen);

    for (size_t i = 0; i < bin.length(); ++i)
    {
        hex[hexlen - 2 * i - 2] = hexstr[(bin[i] >> 4) & 0xF];
        hex[hexlen - 2 * i - 1] = hexstr[bin[i] & 0xF];
    }

    return hex;
}